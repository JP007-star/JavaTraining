import java.util.*;

class IfElseIfDemo {
    public static void main(String args[])
    {
        int i = 15;

        // if else ladder
        if (i == 10) // Relational Operator
            System.out.println("i is 10");
        else if (i == 15)
            System.out.println("i is 15");
        else if (i == 20)
            System.out.println("i is 20");
        else
            System.out.println("i is not present");
    }
}